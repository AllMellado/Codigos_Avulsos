    private void bConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConectarActionPerformed
        bConectar.setEnabled(false);
        bDesconctar.setEnabled(true);
        
        String ip = tIp.getText();
        int porta = Integer.parseInt(tPorta.getText());
        
        try {
            clientSocket = new Socket(ip, porta);
            output = new PrintWriter(clientSocket.getOutputStream(), true);
            input = new BufferedReader(new InputStreamReader(clientSocket.getInputStream()));
        } catch (IOException ex) {
            System.out.println("Deu ruim");
            System.exit(1);
        }
        output.println(tName.getText());
        try {
            gettMsgns().append(input.readLine()+"\n");
        } catch (IOException ex) {
            Logger.getLogger(telaClient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_bConectarActionPerformed

    private void bDesconctarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDesconctarActionPerformed
        bConectar.setEnabled(true);
        bDesconctar.setEnabled(false);
        
        output.println("end");
        output.close();
        try {
            input.close();
            clientSocket.close();
        } catch (IOException ex) {
            System.out.println("deu mais ruim");
        }
        
    }//GEN-LAST:event_bDesconctarActionPerformed

