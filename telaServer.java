    private void bCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCarregarActionPerformed
        port = Integer.parseInt(tPorta.getText());
        server = new ServerThread(this);
        try {
            serverSocket = server.startServer(port);
        } catch (IOException e) {
            System.err.println("Could not listen on port: " + port);
            System.exit(1);
        }
        bCarregar.setEnabled(false);
    }//GEN-LAST:event_bCarregarActionPerformed

